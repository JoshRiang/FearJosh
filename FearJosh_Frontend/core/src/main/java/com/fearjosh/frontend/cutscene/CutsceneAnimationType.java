package com.fearjosh.frontend.cutscene;

/**
 * Types of animations available for cutscene layers.
 */
public enum CutsceneAnimationType {
    NONE, // No animation
    ZOOM_IN, // Gradually zoom in
    ZOOM_OUT, // Gradually zoom out
    PAN_LEFT, // Pan/slide to the left
    PAN_RIGHT, // Pan/slide to the right
    PAN_UP, // Pan/slide up
    PAN_DOWN // Pan/slide down
}
